package com.model;

public class Foods {
  private String food_id;     //定义食物id
  private String food_name;
  private float food_price;     
  private String food_info;
  
  private int isremark;
  private String menu_id;
public String getFood_id() {
	return food_id;
}
public void setFood_id(String food_id) {
	this.food_id = food_id;
}
public String getFood_name() {
	return food_name;
}
public void setFood_name(String food_name) {
	this.food_name = food_name;
}
public float getFood_price() {
	return food_price;
}
public void setFood_price(float food_price) {
	this.food_price = food_price;
}
public String getFood_info() {
	return food_info;
}
public void setFood_info(String food_info) {
	this.food_info = food_info;
}
public int getIsremark() {
	return isremark;
}
public void setIsremark(int isremark) {
	this.isremark = isremark;
}
public String getMenu_id() {
	return menu_id;
}
public void setMenu_id(String menu_id) {
	this.menu_id = menu_id;
}
  
  
  
}
